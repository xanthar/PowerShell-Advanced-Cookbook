# ============================================================================
# ADDING CONTROLS TO A FORM
# ============================================================================
#
# To add controls to your form:
#
# 1. Select a control from the Toolbox
# 2. Click and drag on the form to place and size the control
# 3. Use the Properties panel to configure the control
#
# Common Controls:
# ┌─────────────────────────────────────────────────────────────────────────┐
# │ Control      │ Purpose                           │ Key Properties       │
# ├─────────────────────────────────────────────────────────────────────────┤
# │ Button       │ Trigger actions on click          │ Text, Enabled        │
# │ Label        │ Display static text               │ Text, Font           │
# │ TextBox      │ User text input                   │ Text, Multiline      │
# │ ComboBox     │ Dropdown selection                │ Items, SelectedItem  │
# │ ListBox      │ List of selectable items          │ Items, SelectionMode │
# │ CheckBox     │ Boolean on/off toggle             │ Checked, Text        │
# │ RadioButton  │ Mutually exclusive options        │ Checked, GroupName   │
# │ DataGridView │ Tabular data display              │ DataSource, Columns  │
# │ ProgressBar  │ Progress indication               │ Value, Maximum       │
# │ RichTextBox  │ Formatted text input/display      │ Text, Rtf            │
# └─────────────────────────────────────────────────────────────────────────┘

