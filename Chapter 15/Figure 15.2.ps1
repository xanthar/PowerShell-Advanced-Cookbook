# ============================================================================
# FORM DESIGNER INTERFACE
# ============================================================================
#
# The Form Designer provides a visual WYSIWYG editor for GUI applications:
#
# ┌─────────────────────────────────────────────────────────────────────────┐
# │ [Toolbox]  │  [Form Design Surface]            │  [Properties]          │
# │            │                                    │                        │
# │ Common:    │  ┌────────────────────────────┐   │  Name: Form1           │
# │ ├─ Button  │  │ Form1                      │   │  Text: My Application  │
# │ ├─ Label   │  │                            │   │  Size: 400, 300        │
# │ ├─ TextBox │  │  [Label1]                  │   │  StartPosition:        │
# │ ├─ ComboBox│  │  [TextBox1          ]      │   │    CenterScreen        │
# │ ├─ ListBox │  │  [Button1]                 │   │                        │
# │ └─ ...     │  │                            │   │  Events:               │
# │            │  │                            │   │  ├─ Load               │
# │ Containers:│  │                            │   │  ├─ Click              │
# │ ├─ Panel   │  └────────────────────────────┘   │  └─ Closing            │
# │ └─ GroupBox│                                    │                        │
# └─────────────────────────────────────────────────────────────────────────┘
#
# Key features:
# - Drag controls from Toolbox to Form
# - Set properties in the Properties panel
# - Double-click controls to create event handlers
# - Snap-to-grid alignment for precise layouts

