# This figure shows a screenshot from the AWS Console
# demonstrating IAM user creation through the web interface.
#
# To create an IAM user in the AWS Console:
# 1. Navigate to IAM > Users > Create user
# 2. Enter a username
# 3. Select access type (Console and/or Programmatic)
# 4. Assign permissions via groups or policies
# 5. Review and create the user
# 6. Download or copy the Access Key ID and Secret Access Key
#
# NOTE: Access keys are only shown once at creation time.
# Store them securely - they cannot be retrieved later.
